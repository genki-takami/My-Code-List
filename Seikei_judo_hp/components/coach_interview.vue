            <h2><span class="accept_space"></span>監督特集</h2>
          </div>
          <div class="article_body">
            <p>2019年11月末に、体育会柔道部広報担当の杉本(1年)と副主務(ICT担当)のタカミは三浦武彦監督に30分にわたるインタビューを実施。何年にもわたり成蹊柔道部を指導してきた監督の柔道人生をダイアローグ形式で見ていくことにしよう。</p>
            <img class="kantoku_image_fit" src="https://drive.google.com/uc?export=view&id=17wV6XLc6YgILU4pn6tv0kxTHTcz8_brq" style="width:100%;" alt="監督">
            <p>ーーよろしくお願いします。</p>
            <p><b>三浦</b><span class="accept_space"></span>(会釈)</p>
            <p>ーーまず初めに、柔道を始めたきっかけを教えて下さい。</p>
            <p><b>三浦</b><span class="accept_space"></span>成蹊中学には昭和56年に入学しまして、中学に入学したときは、小学校でラグビー部に所属していたのでラグビー部に入ろうかなと思っていましたね。ところが、ちょうど入学した年は成蹊学園の柔道場が新築で完成しまして、その時の体育の先生で柔道部の顧問であった吉田先生から色々と話を聞いて、面白そうだなと思ってなんとなく入部したのが柔道を始めるきっかけでした。それから中高大と成蹊で柔道をしていくのですよ。</p>
            <p>ーーありがとうございます。中学時代の柔道部での思い出はどのようなものがありますか？</p>
            <p><b>三浦</b><span class="accept_space"></span>そうですね、中学1年のときに柔道部ができたこともあって、あまり対外的な試合がなかったんですよ。それに部員も少なかったですし。それでも僕としては、自分の実力を確かめたいという思いがありましたので、部活の練習以外にも近くの道場に行ったり、講道館に通ったりして稽古していました。結局中学3年のときに黒帯を取ることができ、自分なりに目標を持ってそれを成し遂げたのは非常に実感しましたし、すごく思い出に残っていますね。</p>
            <p>ーー次に、中学から高校にあがる際にどのような気持ちで柔道を続けようと思ったのですか？</p>
            <p><b>三浦</b><span class="accept_space"></span>まあ、高校生のときは公式戦である高体連の、できれば都大会の上位までいってどこまでいけるのか試してみたいという気持ちでした。はい。</p>
            <p>ーーもしよろしければ、その大会に出場してみて成績はどうだったかお聞かせ下さい。</p>
            <p><b>三浦</b><span class="accept_space"></span>支部予選で敗退しました。高校１年のときは団体戦にしか出場していないのですけど、2年3年次には個人戦にも出場しました。えっと、2年生のときは今でいう73キロ級の中量級で出て、結構良いとこまでいって、コレはいけるんじゃないかと思って、翌年の高校3年のときは減量しまして、60キロ以下で出場したんですけど、結局明大中野の選手に負けてしまいました。判定だったんですけどね、上にはいけませんでしたね。</p>
            <img class="kantoku_image_fit" src="https://drive.google.com/uc?export=view&id=1V6zUsmDy5YMQY_XcOUBHFmCmnAHRoeXC" style="width:100%;" alt="監督">
            <p>ーーなるほど。同じような質問になるのですが、高校時代での思い出はどのようなものがありましたか？</p>
            <p><b>三浦</b><span class="accept_space"></span>そうですね、部員数もだいぶ増えてきてですね、結構部活動としても盛り上がった感じで、非常に良かったんではないでしょうか。中々試合の成績は残せなかったのだけれども、文化祭の招待試合なんかに行ったりして、結構良い試合なんかもしてましたね。団体戦の方も、結果的には上にはいけなっかたにしても、 3対2で中々惜しい結果となったんですよ。そういう意味でも、部としては盛り上がっていたのではないかと感じますね。</p>
            <p>ーー大学に進学するにあたって、柔道から離れる人が多い中、柔道を続けようと思った理由をお聞かせ下さい。</p>
            <p><b>三浦</b><span class="accept_space"></span>そうですね、大学に入ると中学高校までと違って、より自由になりますし、スポーツの数もサークルなど増えますから、何か新しいことを始めようかなと全く思わなかったわけではないですね。やはり継続は力なりというのですかね。やり通して何か得るものがあるのではないかなと思いまして。それと仲間ですかね。高校からあがってきた仲間もいたし、大学から入った仲間もいたし、一緒にいて非常に仲間としてよかったなと思いますし、引き続き柔道を続けようと思いましたね。</p>
            <p>ーーその当時は部員はどれくらいいたのでしょうか？</p>
            <p><b>三浦</b><span class="accept_space"></span>僕らの同期は4人で、全部でたぶん15～16人だったと思います。ただ、僕が1年の時は3年生がいなかったですね、確か。学年によって増えたり減ったりはありましたね。</p>
            <p>ーー大学で柔道を続けるにあたって、吉田先生の影響はやはりあったのでしょうか？</p>
            <p><b>三浦</b><span class="accept_space"></span>そうですね、やはり中学の時から教えてもらっていたので、はい、普段は物腰が柔らかいんですけど、言うべきことはキチッと言ってもらって適確に指導していただいたので、大学まで含めて吉田先生には大変感謝しております。</p>
            <p>ーー次に、大学で生活するにあたって、柔道と学業の両立はどのようにやられていましたか？</p>
            <p><b>三浦</b><span class="accept_space"></span>まぁー、別に何か意識してやってたわけではないんですが、集中力じゃないんですかね。あのー、365日ずっと勉強したり、365日ずっと柔道しているわけにもいかないので、柔道をやるときは集中してやる。その代わり、試験のときは集中して試験に臨むということ、この繰り返しだと思うんですよね。結果的には、社会人になっても思うんですけど、やっぱりいざというときの集中力。明日までに何かしなければならないときの最後の底力じゃないですかね。こういったところが身についたんじゃないかなと思いますね。</p>
            <p>ーー突然ですが、大学での部員確保はどのようにされていましたか？</p>
            <p><b>三浦</b><span class="accept_space"></span>そうですね、今みたいにSNSがあったわけではないので、本当にありきたりですけれど、ポスターを貼ったり、入学式の時には1年生のところに出向いて、声をかけまくって、これだと思った新入生がいたら先輩が席をキープしている喫茶店で話をするということを、地味でしたけれど、はい、やっていましたね。</p>
            <p>ーー参考にします！今の大学の稽古内容と当時の稽古内容に違いなどはあったりしますか？</p>
            <p><b>三浦</b><span class="accept_space"></span>筋トレメニューみたいな補強メニューなんかは稽古の中にあまり入っていないと思うんですよね。どちらかと言うと、稽古が終わった後に各人自主練で筋トレなんかをやっていると思うんだけれども、そういういやり方もあるとは思うのだけれども、僕らのときは補強メニューなんかは準備運動が終わった後にやっていたね。毎回。そして稽古が終わった後に、綱上りなんかをずっと続けてきましたね。それにプラスアルファ筋トレをしたい人なんかは、ベンチプレスをやったりしていましたね。あのー、柔道で使う筋肉って結構独特だと思うんですよ。単純に胸の筋肉を鍛えればよいのではなくて、柔道の動きの中で鍛えていくというのもあるので、補強運動は積み重ねることで意味をなすので、日々の練習の中にもう少し補強運動を入れた方がいいんじゃないかなと思いますね。</p>
            <img class="kantoku_image_fit" src="https://drive.google.com/uc?export=view&id=1Fau4ywGZlU_Y2zACtgJ3L84dON11R9q8" style="width:100%;" alt="監督">
            <p>ーーそうですか。就活で成蹊大学柔道部での経験はどのようにいかされましたか？</p>
            <p><b>三浦</b><span class="accept_space"></span>OBを訪問するにあたって、柔道部に限らず体育会のつながりでお話しできたり、私は銀行員なのですけれども、内定をもらうまでに面接が何回もあって、一貫してやってきた柔道部での活動みたいなものをひたすらお話ししながら進めてきたところがあるので、人のつながりも含めて、非常に柔道部にいたことが役に立ったのではないかなと思います。</p>
            <p>ーーさらに訊いていきます。社会人になって柔道から離れた理由と、また柔道を再びやり始めた理由をお聞かせ下さい。</p>
            <p><b>三浦</b><span class="accept_space"></span>まずひとつは、社会人になって生活がガラッと変わったことと、もうひとつは最初の赴任地が四国の松山だったんですよ。それで中々近くで柔道場がなかった環境面とやはり仕事の忙しさですかね。社会人10年目くらいまでは、自分の仕事が身につくまで忙しいですから。それと、当時は今と違って残業が多かったので、中々柔道ができる環境ではなかったですね。そうこうしているうちに、2008年の北京オリンピックでの柔道の試合を見て、日本の柔道はこんなに弱いはずじゃないと勝手に思い始めて、なんとなく近くの道場に行って再開したのが始まりですね。</p>
            <p>ーーそのときの体力はいかほどでしたか？</p>
            <p><b>三浦</b><span class="accept_space"></span>もうめちゃくちゃでしたね。乱取り1本した瞬間に気持ち悪くなって、全然ダメだなと思いましたね。</p>
            <p>ーー失礼ながらお腹の方は出ていたのでしょうか？</p>
            <p><b>三浦</b><span class="accept_space"></span>今は68キロくらいなんですけれども、当時は77～78キロくらいありましたからね。そこから、杉並区民大会の3段の部で準優勝しまして、まだまだいけるじゃんと思いましたね。</p>
            <p>ーーブランクがあって、乱取り1本も厳しかったのにも関わらず柔道を続けようとしたのは、どのような動機があったのでしょうか？</p>
            <p><b>三浦</b><span class="accept_space"></span>色々ありますけど、まずひとつはどこまでできるか分からないけど、やってみよう、ということでやり始めました。あと、成蹊大学の柔道部にも毎週土曜日に顔を出すようになって、やっぱり後輩には負けられないと思いまして。そうこうしているうちに、昇段審査をしてみないかという声をかけてもらって、チャレンジしてみて、すぐに4段が取れて、どこまでいけるかな、試してみようかなと思って、はい、続けてきました。</p>
            <p>ーー次に、柔道のルールが変わったことに関してはどうお考えですか？</p>
            <p><b>三浦</b><span class="accept_space"></span>競技柔道としては良くなっていく方向ではあると思うんですよね。やっぱり、昨今のスポーツをめぐる動きっていうのは、やはり、見ている人が見ていて分からないと、やはり、全然盛り上がらない。素人が見ても面白いと思える競技じゃないと、中々競技人口は増えないし、あるいはオリンピックで正式種目になりにくいから、そういう意味では良くなっていると思いますね。ただ、柔道の根幹である戦い方がだいぶ失われている部分があるんじゃないかなとも。</p>
            <img class="kantoku_image_fit" src="https://drive.google.com/uc?export=view&id=1Oohjv7KjC0WaNNPaTa7STR2nIkBcOgP9" style="width:100%;" alt="監督">
            <p>ーーすくい投げとか、できなくなった技とかもありましたね。</p>
            <p><b>三浦</b><span class="accept_space"></span>今のルールは体重別が前提となっているから言いにくいのだけれども、もともと柔道は体重別という概念がないので、それこそ団体戦なんかでは小さい人がでかい人と戦わなくてはいけなくて、ガッツリ組み合ったら絶対にでかい人が勝つんですよ。だから、小さい人が組ませない。そして組んだ瞬間に投げる。今のルールだとすぐに反則しちゃうので、やっぱり今のルールでは柔道の本質に沿ったルールではない、どうしても競技柔道、体重別前提のルールになっている、そう思いますね。</p>
            <p>ーー監督が勤め先に柔道部を作ったとされているのを耳にしたのですが、それはどういう経緯で…</p>
            <p><b>三浦</b><span class="accept_space"></span>大学でもOBとして稽古をし、杉並区柔道会でも練習していく中で、会社の中にも柔道経験者はいるよねと思いまして、柔道部をつくってみたらどうかなと思いまして。それに実業団の大会にも出れると思いましてね、より柔道の世界が広がるかなと</p>
            <p>ーー先ほどから話を伺っていて感じるのですが、ご自身でチャレンジスピリッツがある方だと思いますか？</p>
            <p><b>三浦</b><span class="accept_space"></span>うーん、結果的にはそうなるかもしれませんね。僕自身、自分がメチャクチャ強いと思っているわけではないんですよ。柔道を再開してから42～3歳ごろに骨折は3回くらいしているし、仕事もギプスをはめながら行ったこともありますしね。でもね、怪我したらどうしたら怪我しやすいか、どれくらいで治るのかが経験値でついてくるので、そうすると学生が怪我をしたときにどれくらいで治るだろうかとか、色々と知識がついてくるので、怪我は痛いですけども、それもありなんかじゃないかなと</p>
            <p>ーーそれに関して家族はどう感じていましたか？</p>
            <p><b>三浦</b><span class="accept_space"></span>まぁ、特になにも言わずですね。</p>
            <p>ーー最後に、現役部員とこれから成蹊大学柔道部に入る生徒になにかあればお願いします。</p>
            <p><b>三浦</b><span class="accept_space"></span>成蹊大学体育会柔道部というのは、体育会柔道部の中ではいわゆる1部校とは違って人数も少ないですし、決して強い大学ではありません。でも一方で、大学から始めた白帯の子も何人もいますし、素人の子でも入部してできる。こういう柔道部だと思うのですよ。稽古は白帯の子も黒帯の子も含めてできるような、そういうメニューで極力短い時間で集中してもらう、というのをモットーとしてね。そして、ある意味自由な環境の中で自分自身を鍛えることができる、そういう柔道だと思いますので、ぜひ成蹊の柔道部に興味があったら覗いてみてもらえればと思います。</p>
            <img class="kantoku_image_fit" src="https://drive.google.com/uc?export=view&id=1COrP8zxaRtp9hCTO6dnoD6H2ffmNqb2T" style="width:100%;" alt="監督">
            <p>ーー今回は貴重なお時間ありがとうございました。</p>
            <p><b>三浦</b><span class="accept_space"></span>ありがとうございまいた。</p>
